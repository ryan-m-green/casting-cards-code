$scssFiles = Get-ChildItem -Path "C:\Repository\NPC Library\code\frontend\cast-library-ui\src" -Recurse -Filter "*.scss" | Where-Object { $_.Name -ne "_variables.scss" }

$log = @()

foreach ($file in $scssFiles) {
  $content = [System.IO.File]::ReadAllText($file.FullName)
  $original = $content
  foreach ($old in $map.Keys) {
    $new = $map[$old]
    $oldVar = "v.`$$old"
    $newVar = "v.`$$new"
    if ($content.Contains($oldVar)) {
      $count = ([regex]::Matches($content, [regex]::Escape($oldVar))).Count
      $log += "$($file.Name) [$count] $oldVar -> $newVar"
      $content = $content.Replace($oldVar, $newVar)
    }
  }
  if ($content -ne $original) {
    [System.IO.File]::WriteAllText($file.FullName, $content)
  }
}

$log | ForEach-Object { Write-Output $_ }
Write-Output ""
Write-Output "Done. $($log.Count) replacement entries."
